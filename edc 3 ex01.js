//1-	Fazer um programa que imprima os números
 //de 1 até um valor informado pelo usuário;

 function imprimirNumeros() {
    let valor = parseInt(prompt("Digite um valor para imprimir os números de 1 até esse valor:"));

    if (isNaN(valor) || valor <= 0) {
        alert("Por favor, digite um número válido maior que 0.");
        return;
    }

    for (let i = 1; i <= valor; i++) {
        console.log(i);
    }
}

imprimirNumeros();
